package sv.edu.ues.eisi.fia.procesosadministrativosfia;

public class DetalleEstudianteRepetido {
    private String carnet, idDetalleEstuRep, idDetalleDifRep, fechaInscripcion;

    public String getCarnet() {
        return carnet;
    }

    public void setCarnet(String carnet) {
        this.carnet = carnet;
    }

    public String getIdDetalleEstuRep() {
        return idDetalleEstuRep;
    }

    public void setIdDetalleEstuRep(String idDetalleEstuRep) {
        this.idDetalleEstuRep = idDetalleEstuRep;
    }

    public String getIdDetalleDifRep() {
        return idDetalleDifRep;
    }

    public void setIdDetalleDifRep(String idDetalleDifRep) {
        this.idDetalleDifRep = idDetalleDifRep;
    }

    public String getFechaInscripcion() {
        return fechaInscripcion;
    }

    public void setFechaInscripcion(String fechaInscripcion) {
        this.fechaInscripcion = fechaInscripcion;
    }
}
